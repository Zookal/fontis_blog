class Fontis_Blog_Helper_Comment extends Mage_Core_Helper_Abstract
{
    public function renderPage(Mage_Core_Controller_Front_Action $action, $identifier = null, $data)
    {
        $page = Mage::getSingleton('blog/post');
        if (!is_null($identifier) && $identifier !== $page->getId()) {
            $page->setStoreId(Mage::app()->getStore()->getId());
            if (!$page->load($identifier)) {
                return false;
            }
        }

        if (!$page->getId()) {
            return false;
        }
        if ($page->getStatus() == 2) {
            return false;
        }

        $action->loadLayout();
        if ($storage = Mage::getSingleton('customer/session')) {
            $action->getLayout()->getMessagesBlock()->addMessages($storage->getMessages(true));
        }
        $blogTitle = Mage::getStoreConfig('fontis_blog/blog/title') . " - ";
        $action->getLayout()->getBlock('head')->setTitle($blogTitle . Mage::getSingleton('blog/post')->load($identifier)->getTitle());
        $action->getLayout()->getBlock('root')->setTemplate(Mage::getStoreConfig('fontis_blog/blog/layout'));
        $action->getLayout()->getBlock('post')->setCommentDetails($data['user'], $data['email'], $data['comment']);
        $action->renderLayout();

        return true;
    }
}
